-- 1. FUNCIÓN Y TRIGGER PARA SUPABASE AUTH
-- Esto inserta automáticamente un registro en tu tabla 'usuario' 
-- cada vez que alguien se registra en el sistema de autenticación de Supabase.
-- ==============================================================================

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.usuario (id_auth, correo, nombres, apellidos, es_miembro_activo)
  VALUES (
    new.id, 
    new.email, 
    -- Supabase permite pasar metadata al registrarse (ej. nombres y apellidos). 
    -- Usamos COALESCE por si se registran sin enviar esos datos, para que no falle el NOT NULL.
    COALESCE(new.raw_user_meta_data->>'nombres', 'Usuario Nuevo'), 
    COALESCE(new.raw_user_meta_data->>'apellidos', 'Por actualizar'),
    TRUE
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Eliminamos el trigger si existe para evitar errores al re-ejecutar
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();


-- ==============================================================================
-- 2. POLÍTICAS DE SEGURIDAD RLS (Row Level Security)
-- ==============================================================================

-- ---------------------------------------------------------
-- POLÍTICAS GENERALES (Catálogos y vistas públicas para el comité)
-- Permite que cualquier miembro logueado pueda VER la información base.
-- ---------------------------------------------------------
CREATE POLICY "Lectura pública de áreas" ON area FOR SELECT TO authenticated USING (true);
CREATE POLICY "Lectura pública de cargos" ON cargo FOR SELECT TO authenticated USING (true);
CREATE POLICY "Lectura pública de oportunidades" ON oportunidad FOR SELECT TO authenticated USING (true);
CREATE POLICY "Lectura pública de logros" ON logro_gamificacion FOR SELECT TO authenticated USING (true);
CREATE POLICY "Lectura pública de eventos" ON evento FOR SELECT TO authenticated USING (true);
CREATE POLICY "Lectura pública de metas KPI" ON meta_kpi FOR SELECT TO authenticated USING (true);
CREATE POLICY "Lectura pública de plantillas" ON tarea_plantilla FOR SELECT TO authenticated USING (true);

-- ---------------------------------------------------------
-- POLÍTICAS DE USUARIO (Perfiles)
-- Todos pueden ver a los demás miembros del comité, pero solo uno mismo puede editar su perfil.
-- ---------------------------------------------------------
CREATE POLICY "Ver perfiles del comité" ON usuario FOR SELECT TO authenticated USING (true);

CREATE POLICY "Actualizar perfil propio" ON usuario FOR UPDATE TO authenticated 
USING (id_auth = auth.uid()) 
WITH CHECK (id_auth = auth.uid());

-- ---------------------------------------------------------
-- POLÍTICAS DE TAREAS Y ASISTENCIA
-- ---------------------------------------------------------
-- Todos pueden ver las tareas y asistencias (para transparencia en el equipo)
CREATE POLICY "Ver todas las tareas" ON tarea FOR SELECT TO authenticated USING (true);
CREATE POLICY "Ver todas las asistencias" ON asistencia FOR SELECT TO authenticated USING (true);
CREATE POLICY "Ver detalles de tareas" ON tarea_detalle FOR SELECT TO authenticated USING (true);

-- (Opcional) Solo el usuario asignado puede actualizar el estado de su tarea
CREATE POLICY "Actualizar tareas propias" ON tarea FOR UPDATE TO authenticated 
USING (
  id_usuario_asignado = (SELECT id_usuario FROM usuario WHERE id_auth = auth.uid())
);

-- ---------------------------------------------------------
-- PERMISOS DE INSERCIÓN/CREACIÓN (Para desarrollo)
-- *Nota: En producción podrías restringir esto solo a LCP/LCVP.
-- Por ahora, permitiremos que usuarios autenticados inserten datos básicos.
-- ---------------------------------------------------------
CREATE POLICY "Crear tareas" ON tarea FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Marcar asistencia" ON asistencia FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Crear eventos" ON evento FOR INSERT TO authenticated WITH CHECK (true);